#!/usr/bin/env node
/**
 * generate-metadata.mjs
 *
 * Build-time script: scans all JSON files in frontend/public/data/ for
 * transaction `date` fields, finds the maximum date, and writes it to
 * frontend/src/generated/metadata.ts as a typed constant.
 *
 * Usage: node scripts/generate-metadata.mjs
 */

import { readFileSync, readdirSync, mkdirSync, writeFileSync } from "fs";
import { join, dirname } from "path";
import { fileURLToPath } from "url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, "..");
const DATA_DIR = join(ROOT, "frontend", "public", "data");
const OUT_DIR = join(ROOT, "frontend", "src", "generated");
const OUT_FILE = join(OUT_DIR, "metadata.ts");

/** Recursively extract all `date` values from a parsed JSON value. */
function extractDates(value, acc = []) {
  if (Array.isArray(value)) {
    value.forEach((v) => extractDates(v, acc));
  } else if (value && typeof value === "object") {
    if (typeof value.date === "string" && /^\d{4}-\d{2}-\d{2}$/.test(value.date)) {
      acc.push(value.date);
    }
    Object.values(value).forEach((v) => extractDates(v, acc));
  }
  return acc;
}

let maxDate = "";

for (const file of readdirSync(DATA_DIR)) {
  if (!file.endsWith(".json")) continue;
  try {
    const raw = JSON.parse(readFileSync(join(DATA_DIR, file), "utf8"));
    const dates = extractDates(raw);
    for (const d of dates) {
      if (d > maxDate) maxDate = d;
    }
  } catch {
    // Skip malformed files
  }
}

if (!maxDate) {
  console.warn("[generate-metadata] Warning: no date fields found. Using fallback.");
  maxDate = new Date().toISOString().slice(0, 10);
}

mkdirSync(OUT_DIR, { recursive: true });
writeFileSync(
  OUT_FILE,
  `// AUTO-GENERATED by scripts/generate-metadata.mjs — do not edit manually
// Re-run: node scripts/generate-metadata.mjs

/** ISO date of the most recent transaction in the loaded dataset. */
export const DATA_AS_OF = "${maxDate}" as const;
`,
);

console.log(`[generate-metadata] DATA_AS_OF = ${maxDate} → ${OUT_FILE}`);

#!/usr/bin/env node
/**
 * generate-metadata.mjs
 *
 * Build-time script: scans all JSON files in frontend/public/data/ for
 * transaction `date` fields, finds the maximum date, and writes it to
 * frontend/src/generated/metadata.ts as a typed constant.
 *
 * Usage:
 *   node scripts/generate-metadata.mjs           # dev/lint mode — writes placeholder if data missing
 *   node scripts/generate-metadata.mjs --strict  # CI/build mode — exits 1 if data missing or no dates
 */

import { readFileSync, readdirSync, mkdirSync, writeFileSync } from "fs";
import { join, dirname } from "path";
import { fileURLToPath } from "url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, "..");
const DATA_DIR = join(ROOT, "frontend", "public", "data");
const OUT_DIR = join(ROOT, "frontend", "src", "generated");
const OUT_FILE = join(OUT_DIR, "metadata.ts");

const STRICT = process.argv.includes("--strict");

/** Recursively extract all `date` values from a parsed JSON value. */
function extractDates(value, acc = []) {
  if (Array.isArray(value)) {
    value.forEach((v) => extractDates(v, acc));
  } else if (value && typeof value === "object") {
    if (typeof value.date === "string" && /^\d{4}-\d{2}-\d{2}$/.test(value.date)) {
      acc.push(value.date);
    }
    Object.values(value).forEach((v) => extractDates(v, acc));
  }
  return acc;
}

/** Write metadata file with the given date value (may be a placeholder). */
function writeMetadata(dateValue, placeholder = false) {
  mkdirSync(OUT_DIR, { recursive: true });
  const comment = placeholder
    ? "// NOTE: placeholder date — data directory not found. Run data-download step."
    : "";
  writeFileSync(
    OUT_FILE,
    `// AUTO-GENERATED by scripts/generate-metadata.mjs — do not edit manually
// Re-run: node scripts/generate-metadata.mjs
${comment}
/** ISO date of the most recent transaction in the loaded dataset. */
export const DATA_AS_OF = "${dateValue}" as const;
`,
  );
}

let maxDate = "";

let dataFiles;
try {
  dataFiles = readdirSync(DATA_DIR).filter((f) => f.endsWith(".json"));
} catch (err) {
  if (err.code === "ENOENT") {
    if (STRICT) {
      console.error(`[generate-metadata] ERROR: data directory not found: ${DATA_DIR}`);
      console.error("  Run data-download step first, or check your working directory.");
      process.exit(1);
    } else {
      // Dev/lint mode: write placeholder so TypeScript doesn't fail on missing import
      const placeholder = "1970-01-01";
      console.warn(`[generate-metadata] Warning: data directory not found — writing placeholder ${placeholder}`);
      writeMetadata(placeholder, true);
      process.exit(0);
    }
  } else {
    console.error(`[generate-metadata] ERROR reading data directory: ${err.message}`);
    process.exit(1);
  }
}

for (const file of dataFiles) {
  try {
    const raw = JSON.parse(readFileSync(join(DATA_DIR, file), "utf8"));
    const dates = extractDates(raw);
    for (const d of dates) {
      if (d > maxDate) maxDate = d;
    }
  } catch (err) {
    console.warn(`[generate-metadata] Warning: skipping ${file} — ${err.message}`);
  }
}

if (!maxDate) {
  console.error("[generate-metadata] ERROR: no date fields found in any data file.");
  console.error("  Ensure frontend/public/data/ exists and contains valid JSON with 'date' fields.");
  process.exit(1);
}

writeMetadata(maxDate);
console.log(`[generate-metadata] DATA_AS_OF = ${maxDate} → ${OUT_FILE}`);
